// Relational Operators - gives answers in true or false
// 1. ==
// 2. !=
// 3. >
// 4. <
// 5. >=
// 6. <=

public class J02_RelationalOperators {
    public static void main(String[] args) {
        int A = 10;
        int B = 5;
        System.out.println("== : " + (A == B));
        System.out.println("!= : " + (A != B));
        System.out.println("> : " + (A > B));
        System.out.println("< : " + (A < B));
        System.out.println(">= : " + (A >= B));
        System.out.println("<= : " + (A <= B));
    }
}
