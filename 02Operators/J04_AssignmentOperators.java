// Assignment Operators -> these run faster then normal used in competitive programming
// = , += , -= , *= , /= ,%=

public class J04_AssignmentOperators {
    public static void main(String[] args) {
        int A = 10;
        A += 5; // A = A + 5
        System.out.println(A);

        int B = 10;
        B -= 5; // B = B - 5
        System.out.println(B);

        int C = 10;
        C *= 5; // C = C * 5
        System.out.println(C);

        int D = 10;
        D /= 5; // D = D +/ 5
        System.out.println(D);

        int E = 10;
        E %= 5; // E = E % 5
        System.out.println(E);

    }
}
