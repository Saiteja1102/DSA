public class J04_variables {
    public static void main(String args[]){
        // Variables - that store value inside it and values can be changed later on..
        // variables are like storage units its stores information

        // declaring a variable
        // Datatype nameofthevariable = value;
        int number = 11;
        String name = "Sai Teja";

        System.out.println(number);
        System.out.println(name);

        // changed
        number = 8;
        System.out.println(number);
    }
}
