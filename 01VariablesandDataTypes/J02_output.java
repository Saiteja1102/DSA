public class J02_output {
    public static void main(String args[]){
        // 1. print
        System.out.print("Hello print....");
        // 2. println - for the next line after the ending
        System.out.println("Hello println....");
        // 3. \n - wherever it is written it goes to next line
        System.out.print("Hello \nbackslash \nn");
    }
}
